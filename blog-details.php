                                <h2>
                                    <p>My Thoughts...My Experience</p>
                                </h2>
                                <ul>
                                    <li>Post By: <b>Padmapriya M</b></li>
                                    <li><b>20 AUG, 2019</b></li>

                                </ul>

                                <p>There are only a handful of big decisions I have made in my life. Some have affected my life directly, and some the lives of my loved ones. There are ones that have an immediate impact. And some that show their results
                                    only in the distant future.<br> The decisions that have demanded the most careful consideration and have caused the most anxiety and sleepless nights are the ones that have a long term impact on the life of someone
                                    I love. And deciding on the school for my child tops that list. <br> Because it is not just a matter of giving your child an education, but shaping the person he / she will become. It is really a decision about where
                                    your child will spend the most time in what will be the most malleable, creative and joyous phase of his / her life.<br> Like most parents there were many nights of Googling, reading forums, reaching out and exchanging
                                    emails with others who’ve been here. School visits ensued. I set a lot by what ‘feels right’ and honestly, nothing did.<br> Till the day, three years ago, when I walked into a Waldorf Kindergarten in Indira Nagar. The
                                    feeling that this was right for my child has only grown stronger with time, borne out by the fact that missing school is the one thing my child never wants to do.<br> Over the last three years, I have had the privilege
                                    of experiencing first-hand what the Waldorf education offers to a child. Waldorf education, also known as Steiner education, is based on the educational philosophy of Rudolf Steiner. The Austrian philosopher and social
                                    reformer, created Anthroposophy – a human oriented spiritual philosophy that reflects and speaks to the basic deep spiritual questions of humanity, to our basic artistic needs, to the need to relate to the world out
                                    of a scientific attitude of mind. Anthroposophy has found wide applications, from farming to medicine to education. The education system was developed close to a century ago and is today a worldwide movement with over
                                    1000 schools.<br> If you were to Google ‘Waldorf method’ you will find a lot on the system, the pedagogy, the network of schools in different parts of the world etc. My attempt is to offer a slightly different perspective
                                    – as a parent, a relative newbie to the system and as an adult from a culture that doesn’t stray too far from the ‘mainstream’. <br> My observations guide my perspective. I’ve tried to marry what I learn of the system
                                    with what I see my child experiencing. And in so far as this journey is a new one, my observations are by no means all encompassing.<br></p>

                                <span>Learning made for the child</span>

                                <p>We live in a world where there is a Herculean syllabus that a child has to tackle and master – the child adapts to the learning when really it has to be the other way round. At the heart of the Waldorf method lies the belief
                                    that learning has to be driven from the child. So the system is created around age appropriate learning in sync with the natural development of the child. The beauty of this that children learn what they do, when they
                                    are ready. And when that happens, they take to it like fish to water. The result, the natural curiosity of the child is rewarded and we create individuals who thrive in an environment of learning.</p>
                                <span>The Hands, The Heart and The Head</span>

                                <p>Is IQ the future? When we look at an individual, how often do we go by what is traditional intelligence i.e IQ? What about the person’s EQ? Their ability to be creative?<br> Is our education system skewed towards only the
                                    IQ? I think we would all agree that the answer would be a resounding yes!<br> Waldorf takes into account the other important aspects of an individual too. It recognizes, accepts and works on their strength and balances
                                    out the weaknesses. And instead of merely focussing on developing the IQ through intellectual learning, the learning takes the form of actual practice with hands, storytelling, delivered through teachers who are role
                                    models worthy of imitation. This means, the head, the hand and the heart of the child are developed holistically.<br></p>
                                <span>Rhythm, reverence & repetition</span>

                                <p>Children are creatures of habit. And the more structured their lives, the more predictable their routines, the more secure the child feels. The Waldorf method brings rhythm into the child’s life – the onus is on parents
                                    to provide this at home and the teachers create this in school. There are daily, weekly and seasonal rhythms. This gives the child a safe, sound structure to grow up in – one that supports him while giving him the security
                                    to explore.</p>

                                <span>An engaged child for the best learning</span>

                                <p>Teaching in a Waldorf school happens through images, movement, exercises, stories and drawing. Written language is first taught. Children then phonetically learns the sound and finally learns to read what they create. Similarly
                                    Maths is taught through juggling, skipping, hopping – where the basics are first understood through the body. There are tangible themes connected with what children see around them. E.g. Seasons & festivals. Children
                                    are steeped in one main lesson for 4 weeks – two hours a day. This does not make them bored. On the contrary, the lessons are carefully planned keeping the child completely engaged targeting the head, heart and hand
                                    that they help the child retain a sense of awe & wonder – keeping him / her engaged in the learning process.</p>

                                <span>Creating a positive world view</span>

                                <p>Of course the world that our children live in is more competitive, crowded and certainly more violent than ever before. And I don’t think any of us can even imagine what it will be like in the future. It would be surely
                                    irresponsible to try to protect them from the world they will have to live in when they grow up. In fact, our responsibility is to give them tools to deal with it. As the complexity of the world increases, so does the
                                    need for creative solutions. And that is what our education system must be geared towards - creating out of the box thinkers. But let us not forget that this will not happen in isolations. After all, children are great
                                    imitators. What are we as adults around them showing them?Every teacher in a Waldorf system aims to be an adult who is worthy of imitation and emulation. They show the children patience and trust. They aim to instil
                                    confidence and acceptance – leading to a self-assured individual who is confident of his place in the world. This is what I understand and see of the Waldorf / Steiner system. When I tell someone about my journey with
                                    Waldorf and my choice for my child, I’m inevitably asked one question. “How will the child cope in the ‘real’ world?” And this is a very valid question that every parent who chooses an alternate methodology of education
                                    faces. And the answer is a profound one. It comes from answering, as parents, how we want our children to be when they grow up. Do we want to instil in them a sense of beauty, confidence and purpose? Do we want them
                                    to follow other people or follow their own heart?</p>
