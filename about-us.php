									<p>Developed by Rudolf Steiner in 1919, Waldorf Education is based on an understanding of human development that addresses the needs of the growing child. Only when the child is ready for first grade, is it appropriate to use the powers of understanding for more abstract matters, including writing, reading, and arithmetic. But, to the child, it is not simply the acquisition of knowledge that is important, it is the process too. And this is where the Waldorf teacher comes in….the teacher must meet the inner need of every child for true authority and provide a secure basis for the child to reach out into the world.

The Waldorf teachers strive to transform education into an art that educates the whole child—the Heart and the Hands, as well as the Head. The College of teachers at every Waldorf school believes that the children should not simply be taught to do artistic activities and manual skills, but they should be taught so-called "non-artistic" academic subjects imaginatively and artistically as well.
